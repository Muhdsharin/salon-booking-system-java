    private void cancelAppointmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelAppointmentButtonActionPerformed
        String customerName = JOptionPane.showInputDialog("Enter the customer name to cancel the appointment:");
    String date = JOptionPane.showInputDialog("Enter the date (dd/mm/yy) of the appointment to cancel:");
    boolean found = false;

    for (int i = 0; i < appointmentIndex; i++) {
        if (appointments[i] != null && appointments[i].getCustomer().getName().equals(customerName) && appointments[i].getDate().equals(date)) {
            Appointment appointmentToRemove = appointments[i]; // Store the appointment to be removed

            //Remove appointment
            for (int j = i; j < appointmentIndex - 1; j++) {
                appointments[j] = appointments[j + 1];
            }
            appointments[appointmentIndex - 1] = null;
            appointmentIndex--;
            found = true;

            Customer customer = appointmentToRemove.getCustomer(); // Get the customer from the appointment to be removed

            JOptionPane.showMessageDialog(this, "Appointment cancelled successfully!");

            // Update the total charge
            double totalCharge = emailToTotalCharge.getOrDefault(customer.getEmail(), 0.0) - appointmentToRemove.getService().getCharge();
            emailToTotalCharge.put(customer.getEmail(), totalCharge);

            // Display the new total charge
            String message = "Appointment cancelled successfully! New total charge: RM " + totalCharge + "\n";
            for (Appointment amount : appointments) {
                if (amount != null && amount.getCustomer().getEmail().equals(customer.getEmail())) {
                    message += "Service: " + amount.getService().getName() + ", Charge: RM " + amount.getService().getCharge() + "\n";
                }
            }
            JOptionPane.showMessageDialog(this, message);
            break;
        }
    }
    if (!found) {
        JOptionPane.showMessageDialog(this, "No appointment found for the given customer and date.");
    }
    }//GEN-LAST:event_cancelAppointmentButtonActionPerformed

