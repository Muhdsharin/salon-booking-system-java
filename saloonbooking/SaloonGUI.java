    private void viewAppointmentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAppointmentsButtonActionPerformed
        if (isEmpty()) {
            JOptionPane.showMessageDialog(this, "No appointment booked.");
        } else {
            StringBuilder appointmentDetails = new StringBuilder();
            for (Appointment appointment : appointments) {
                if (appointment != null) {
                    appointmentDetails.append("Customer: ").append(appointment.getCustomer().getName())
                            .append(", Service: ").append(appointment.getService().getName())
                            .append(", Charge: ").append(appointment.getService().getCharge())
                            .append(", Date: ").append(appointment.getDate())
                            .append(", Time: ").append(appointment.getTime())
                            .append(", Phone Number: ").append(appointment.getCustomer().getPhoneNumber())
                            .append(", E-mail: ").append(appointment.getCustomer().getEmail())
                            .append("\n");
                }
            }
            JOptionPane.showMessageDialog(this, appointmentDetails.toString());
        }
    }//GEN-LAST:event_viewAppointmentsButtonActionPerformed

    private void bookAppointmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookAppointmentButtonActionPerformed
        if (appointmentDateField.getText().isEmpty() && appointmentTimeField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in your information");
        } else {
            String customerName = customerNameField.getText();
            String serviceName = (String) serviceField.getSelectedItem();
            String chargeString = totalChargeField.getText().replaceAll("[^0-9.]", "");

            if (chargeString.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a valid total charge.");
                return;
            }

            double charge = Double.parseDouble(chargeString);
            String date = appointmentDateField.getText();
            String time = appointmentTimeField.getText();
            String phoneNumber = phoneNumberField.getText();
            String email = emailField.getText();

            Customer customer = new Customer(customerName, email, phoneNumber);
            Service service = new Service(serviceName, charge);
            Appointment appointment = new Appointment(customer, service, date, time);

            appointments[appointmentIndex] = appointment; // Add the appointment to the appointments array
            customers[customerIndex] = customer; // Add the customer to the customers array
            appointmentIndex++; // Increment appointment index
            customerIndex++; // Increment customer index

            // Calculate the new total charge
            double totalCharge = emailToTotalCharge.getOrDefault(email, 0.0) + appointment.getService().getCharge();
            emailToTotalCharge.put(email, totalCharge);

            String message = "Appointment booked succesfully!\n";
            message = message + "Your total charge is: RM " + totalCharge + "\n";
            for (Appointment amount : appointments) {
                if (amount != null && amount.getCustomer().getEmail().equals(email)) {
                    message += "Service: " + amount.getService().getName() + ", Charge: RM " + amount.getService().getCharge() + "\n";
                }
            }
            JOptionPane.showMessageDialog(this, message);
            reset();
        }
    }//GEN-LAST:event_bookAppointmentButtonActionPerformed

