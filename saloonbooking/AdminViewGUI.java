    private void displayAppointments() {
        Appointment[] appointments = SaloonGUI.appointments;
        StringBuilder appointmentsString = new StringBuilder();
        for (int i = 0; i < SaloonGUI.appointments.length; i++) {
            Appointment appointment = SaloonGUI.appointments[i];
            if (appointment != null) {
                Customer customer = appointments[i].getCustomer();
                Service service = appointments[i].getService();
                if (appointment.getCustomer() != null && appointment.getService() != null) {
                    appointmentsString.append("Customer: ").append(appointment.getCustomer().getName())
                            .append(", Service: ").append(appointment.getService().getName())
                            .append(", Date: ").append(appointment.getDate())
                            .append(", Time: ").append(appointment.getTime())
                            .append(", Phone Number: ").append(appointment.getCustomer().getPhoneNumber())
                            .append(", E-mail: ").append(appointment.getCustomer().getEmail())
                            .append("\n");
                }
            }
        }
        displayData_Area.setText(appointmentsString.toString());
    }//GEN-LAST:event_AddDataButtonActionPerformed

    private void deleteDataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDataButtonActionPerformed
    String customerName = JOptionPane.showInputDialog(this, "Enter the customer name to delete:");
    boolean customerFound = false;
    boolean appointmentFound = false;

    for (int i = 0; i < SaloonGUI.customers.length; i++) {
        if (SaloonGUI.customers[i] != null &&
            SaloonGUI.customers[i].getName().equalsIgnoreCase(customerName)) {

            SaloonGUI.customers[i] = null;
            customerFound = true;
            break;
        }
    }

    if (customerFound) {
        for (int i = 0; i < SaloonGUI.appointments.length; i++) {
            if (SaloonGUI.appointments[i] != null &&
                SaloonGUI.appointments[i].getCustomer().getName().equalsIgnoreCase(customerName)) {

                for (int j = i; j < SaloonGUI.appointments.length - 1; j++) {
                    SaloonGUI.appointments[j] = SaloonGUI.appointments[j + 1];
                }

                SaloonGUI.appointments[SaloonGUI.appointments.length - 1] = null;
                appointmentFound = true;
            }
        }

        if (appointmentFound) {
            JOptionPane.showMessageDialog(this, "Customer appointments deleted successfully!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "No customer found with the given name.");
    }

    displayAppointments();
    }//GEN-LAST:event_deleteDataButtonActionPerformed

