private static Staff[] staffList = new Staff[10];
private static int staffIndex = 0;

private void deleteStaff() {
    String name = JOptionPane.showInputDialog(this, "Enter staff name to delete:");

    if (name == null || name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Invalid input!");
        return;
    }

    boolean found = false;

    for (int i = 0; i < staffIndex; i++) {
        if (staffList[i] != null && staffList[i].getName().equalsIgnoreCase(name)) {

            // shift array
            for (int j = i; j < staffIndex - 1; j++) {
                staffList[j] = staffList[j + 1];
            }

            staffList[staffIndex - 1] = null;
            staffIndex--;

            found = true;
            break;
        }
    }

    if (found) {
        JOptionPane.showMessageDialog(this, "Staff deleted successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Staff not found!");
    }
}

private void addStaff() {
    String name = JOptionPane.showInputDialog(this, "Enter staff name:");
    String role = JOptionPane.showInputDialog(this, "Enter staff role:");

    if (name == null || role == null || name.isEmpty() || role.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Invalid input!");
        return;
    }

    if (staffIndex < staffList.length) {
        staffList[staffIndex] = new Staff(name, role);
        staffIndex++;
        JOptionPane.showMessageDialog(this, "Staff added successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Staff list is full!");
    }
}
private void displayStaff() {
    StringBuilder staffInfo = new StringBuilder();

    for (int i = 0; i < staffList.length; i++) {
        if (staffList[i] != null) {
            staffInfo.append("Name: ")
                     .append(staffList[i].getName())
                     .append(", Role: ")
                     .append(staffList[i].getRole())
                     .append("\n");
        }
    }

    if (staffInfo.length() == 0) {
        displayData_Area.setText("No staff available.");
    } else {
        displayData_Area.setText(staffInfo.toString());
    }
}
